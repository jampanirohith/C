//Relational operators
#include<stdio.h>
int main()
{
	int a, b;
			
		printf("Give two numbers: ");
			scanf("%d%d", &a, &b);
		printf("Return value of (%d == %d) is %d\n", a, b, a == b); 
		printf("Return value of (%d != %d) is %d\n", a, b, a != b); 
		printf("Return value of (%d < %d) is %d\n", a, b, a < b); 
		printf("Return value of (%d <= %d) is %d\n", a, b, a <= b); 
		printf("Return value of (%d > %d) is %d\n", a, b, a > b); 
		printf("Return value of (%d >= %d) is %d\n", a, b, a >= b); 

return 0;

}
 
 

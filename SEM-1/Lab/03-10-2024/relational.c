//Relational operators
#include<stdio.h>
int main()
{
	int a=3, b=7;

		printf("Return value of (%d == %d) is %d\n", a, b, a == b); 
		printf("Return value of (%d != %d) is %d\n", a, b, a != b); 
		printf("Return value of (%d < %d) is %d\n", a, b, a < b); 
		printf("Return value of (%d <= %d) is %d\n", a, b, a <= b); 
		printf("Return value of (%d > %d) is %d\n", a, b, a > b); 
		printf("Return value of (%d >= %d) is %d\n", a, b, a >= b); 

return 0;

}
 
 
